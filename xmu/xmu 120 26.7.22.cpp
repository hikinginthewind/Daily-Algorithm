/*
dp线性规划 从尾找到头 找到每个点到最低点的最大值
避免从头找到尾出现选择性错误 
*/
#include <bits/stdc++.h>
using namespace std;
int main()
{
	int n;
	cin >> n;
	vector<vector<int>> g(n,vector<int>(n,0));
	for(int i = 0;i < n;i++)
	{
		for(int j = 0;j <= i;j++)
		{
			cin >> g[i][j];
		}
	}
	vector<vector<int>> dp(n,vector<int>(n));
	for(int i = n - 1;i >= 0;i--)
	{
		if(i == n - 1)
		{
			for(int j = 0;j < n;j++)
			{
				dp[i][j] = g[i][j];
			}
			continue;
		}
		for(int j = 0;j <= i;j++)
		{
			dp[i][j] = max(dp[i + 1][j],dp[i + 1][j + 1]) + g[i][j];
		}
	}
	cout << dp[0][0];
	return 0;
} 
