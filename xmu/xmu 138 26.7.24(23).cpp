#include <bits/stdc++.h>
using namespace std;
int main()
{
	int t;
	cin >> t;
	for(int i = 0;i < t;i++)
	{
		int r,c;
		cin >> r >> c;
		vector<vector<int>> g(r,vector<int>(c));
		for(int j = 0;j < r;j++)
		{
			for(int k = 0;k < c;k++)
			{
				cin >> g[j][k];
			}
		}
		vector<vector<int>> dp(r,vector<int>(c));
		dp[0][0] = g[0][0];
		for(int j = 0;j < r;j++)
		{
			for(int k = 0;k < c;k++)
			{
				if(j == 0 && k == 0)
				{
					continue;
				}
				else if(j != 0 && k == 0)
				{
					dp[j][k] = dp[j - 1][k] + g[j][k];
				}
				else if(j == 0 && k != 0)
				{
					dp[j][k] = dp[j][k - 1] + g[j][k];
				}
				else
				{
					dp[j][k] = max(dp[j - 1][k] + g[j][k],dp[j][k - 1] + g[j][k]);
				}
			}
		}
		cout << dp[r - 1][c - 1] << endl;
	}
	return 0;
} 
