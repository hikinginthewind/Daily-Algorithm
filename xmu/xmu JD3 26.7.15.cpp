#include <bits/stdc++.h>
using namespace std;
int main()
{
	int n,m,q;
	cin >> n >> m >> q;
	vector<vector<int>> pre(n + 1,vector<int>(m + 1));
	for(int i = 1;i < n + 1;i++)
	{
		for(int j = 1;j < m + 1;j++)
		{
			int x;
			cin >> x;
			pre[i][j] = pre[i - 1][j] + pre[i][j - 1] - pre[i - 1][j - 1] + x;
		}
	}
	for(int i = 0;i < q;i++)
	{
		int x1,y1,x2,y2;
		cin >> x1 >> y1 >> x2 >> y2;
		cout << pre[x2][y2] - pre[x1 - 1][y2] - pre[x2][y1 - 1] + pre[x1 - 1][y1 - 1] << endl;
	}
	return 0;
} 
