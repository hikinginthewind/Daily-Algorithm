#include <bits/stdc++.h>
using namespace std;
int main()
{
	int t;
	cin >> t;
	for(int i = 0;i < t;i++)
	{
		int n;
		cin >> n;
		string s;
		cin >> s;
		vector<int> points(n,0);
		for(int j = 0;j < n;j++)
		{
			points[j] = s[j] - '0';
		}
		int l = 0;
		int ans = 0;
		int temp = 0; 
		for(int r = 0;r < n;r++)
		{
			temp += points[r];
			if(r - l + 1 == (n + 1) / 2)
			{
				ans = max(ans,temp);
				temp -= points[l];
				l++; 
			}
		}
		cout << "Case #" << i + 1 << ": " << ans << endl;
	}
	return 0;
} 
